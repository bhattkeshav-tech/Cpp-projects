#include<iostream>
#include<cmath>

using namespace std;

int main(){

  cout<<cbrt(27)<<endl; // prints cube root of 27
  cout<<pow(2,5)<<endl; // prints 2 raised to the power
  cout<<round(4.6)<<endl; // prints 5
  cout<<ceil(4.1)<<endl; // prints 5
  cout<<floor(4.9)<<endl; // prints 4
  cout<<fmax(4,9)<<endl; // prints maximum of two numbers
  cout<<fmin(4,9)<<endl; // prints minimum of two numbers
  cout<<abs(-4)<<endl; // prints absolute value of -4
  cout<<sqrt(16)<<endl; // prints square root of 16
  return 0;



}